void Engine::initializePipelines() {

    VkShaderModule triangleVertexShader;
    VkShaderModule triangleFragShader;

    if (!loadShaderModule("../shaders/vert.spv", &triangleVertexShader)) {
        std::cout << "Failed to load vertex shader\n";
    }
    else {
        std::cout << "Vertex shader loaded successfully\n";
    }

    if (!loadShaderModule("../shaders/frag.spv", &triangleFragShader)) {
        std::cout << "Failed to load fragment shader\n";
    }
    else {
        std::cout <<"Fragment shader loaded successfully\n";
    }


    Pipeline pipeline;
    pipeline.shaderStages.push_back(
            pipeline.pipelineShaderStageCreation(VK_SHADER_STAGE_VERTEX_BIT, triangleVertexShader));
    pipeline.shaderStages.push_back(
            pipeline.pipelineShaderStageCreation(VK_SHADER_STAGE_FRAGMENT_BIT, triangleFragShader));

    pipeline.vertexInputInfo = pipeline.vertexInputStateCreation();
    pipeline.inputAssemblyInfo = pipeline.inputAssemblyStateCreation(VK_PRIMITIVE_TOPOLOGY_TRIANGLE_LIST);

    pipeline.viewport.x = 0.0f;
    pipeline.viewport.y = 0.0f;

    pipeline.viewport.width = static_cast<float>(windowWidth);
    pipeline.viewport.height = static_cast<float>(windowHeight);
    pipeline.viewport.minDepth = 0.0f;
    pipeline.viewport.maxDepth = 1.0f;

    std::vector<VkDynamicState> dynamicStates = {
            VK_DYNAMIC_STATE_VIEWPORT,
            VK_DYNAMIC_STATE_SCISSOR
    };

    VkPipelineDynamicStateCreateInfo dynamicStateCreateInfo{};
    dynamicStateCreateInfo.sType = VK_STRUCTURE_TYPE_PIPELINE_DYNAMIC_STATE_CREATE_INFO;
    dynamicStateCreateInfo.dynamicStateCount = static_cast<uint32_t>(dynamicStates.size());
    dynamicStateCreateInfo.pDynamicStates = dynamicStates.data();

    VkPipelineVertexInputStateCreateInfo vertexInputInfo{};
    vertexInputInfo.sType = VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO;
    vertexInputInfo.vertexBindingDescriptionCount = 0;
    vertexInputInfo.pVertexBindingDescriptions = nullptr;
    vertexInputInfo.vertexAttributeDescriptionCount = 0;
    vertexInputInfo.pVertexAttributeDescriptions = nullptr;

    VkPipelineInputAssemblyStateCreateInfo inputAssembly{};
    inputAssembly.sType = VK_STRUCTURE_TYPE_PIPELINE_INPUT_ASSEMBLY_STATE_CREATE_INFO;
    inputAssembly.topology = VK_PRIMITIVE_TOPOLOGY_TRIANGLE_LIST;
    inputAssembly.primitiveRestartEnable = VK_FALSE;

    VkViewport viewport{};
    viewport.x = 0.0f;
    viewport.y = 0.0f;
    viewport.width = (float)windowWidth;
    viewport.height = (float)windowHeight;
    viewport.minDepth = 0.0f;
    viewport.maxDepth = 1.0f;

    VkRect2D scissor{};
    scissor.offset = {0, 0};
    scissor.extent = swapChainExtent;

    VkPipelineViewportStateCreateInfo viewportStateCreateInfo{};
    viewportStateCreateInfo.sType = VK_STRUCTURE_TYPE_PIPELINE_VIEWPORT_STATE_CREATE_INFO;
    viewportStateCreateInfo.viewportCount = 1;
    viewportStateCreateInfo.scissorCount = 1;
    viewportStateCreateInfo.pViewports = &viewport;
    viewportStateCreateInfo.pScissors = &scissor;

    VkPipelineRasterizationStateCreateInfo rasterizer{};
    rasterizer.sType = VK_STRUCTURE_TYPE_PIPELINE_RASTERIZATION_STATE_CREATE_INFO;
    rasterizer.depthClampEnable = VK_FALSE;
    rasterizer.rasterizerDiscardEnable = VK_FALSE;
    rasterizer.polygonMode = VK_POLYGON_MODE_FILL;
    rasterizer.lineWidth = 1.0f;
    rasterizer.cullMode = VK_CULL_MODE_BACK_BIT;
    rasterizer.frontFace = VK_FRONT_FACE_CLOCKWISE;
    rasterizer.depthBiasEnable = VK_FALSE;
    rasterizer.depthBiasConstantFactor = 0.0f;
    rasterizer.depthBiasClamp = 0.0f;
    rasterizer.depthBiasSlopeFactor  = 0.0f;

    vkDestroyShaderModule(logicalDevice, triangleVertexShader, nullptr);
    vkDestroyShaderModule(logicalDevice, triangleFragShader, nullptr);
}

bool Engine::loadShaderModule(const char *filePath, VkShaderModule *outShaderModule) {

    std::ifstream file(filePath, std::ios::ate | std::ios::binary);

    if (!file.is_open()) {
        return false;
    }

    size_t fileSize = static_cast<size_t>(file.tellg());

    std::vector<uint32_t> buffer(fileSize / sizeof(uint32_t ));

    file.seekg(0);

    file.read((char*)buffer.data(), fileSize);

    file.close();

    VkShaderModuleCreateInfo shaderModuleCreateInfo{};
    shaderModuleCreateInfo.sType = VK_STRUCTURE_TYPE_SHADER_MODULE_CREATE_INFO;
    shaderModuleCreateInfo.pNext = nullptr;
    shaderModuleCreateInfo.codeSize = buffer.size();
    shaderModuleCreateInfo.pCode = buffer.data();

    VkShaderModule shaderModule;
    if (vkCreateShaderModule(logicalDevice, &shaderModuleCreateInfo, nullptr, &shaderModule) != VK_SUCCESS) {
        return false;
    }

    *outShaderModule = shaderModule;
    return true;
}
